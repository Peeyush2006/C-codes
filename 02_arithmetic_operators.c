#include <stdio.h>

int main()
{
    int a = 45 ;
    int b = 18 ;
    int c = a + b ;
    printf("The value of a is %d and the value of b is %d and the sum is %d\n", a,b,c);
    // Modulus operator is used to get remainder
    printf("The remainder when a is divided by b is %d", a%b);
    return 0;
}   